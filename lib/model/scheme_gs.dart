class SchemeResponse {

  late String type;
  late int id;

  SchemeResponse({required this.type, required this.id});

  SchemeResponse.fromJson(Map<String, dynamic> json) {
    type = json['type'];
    id = json['id'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = new Map<String, dynamic>();
    data['type'] = this.type;
    data['id'] = this.id;
    return data;
  }
}